#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int n;

	if (s)
	{
		n = 0;
		while (*(s + n) != '\0')
		{
			write(fd, &*(s + n), 1);
			n++;
		}
	}
}
